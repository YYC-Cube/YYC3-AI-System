export function getI18n(lang: Language): I18nStrings {
  switch (lang) {
    case 'zh':
      return zh;
    case 'ja':
      if (!_ja) {
        _ja = { ...en, ...jaOverrides } as I18nStrings;
      }
      return _ja;
    case 'ko':
      if (!_ko) {
        _ko = { ...en, ...koOverrides } as I18nStrings;
      }
      return _ko;
    default:
      return en;
  }
}
